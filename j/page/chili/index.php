	<h1>Jimm's Venison Chili</h1>
	<h2>Ingredients:</h2>
	<ul>
		<li>1.5lbs cubed venison (other meats should be good too, but I like to use my own venison)
		<li>1 large white onion
		<li>16oz chili beans
		<li>15oz diced tomatoes italian style
		<li>6oz tomato paste
		<li>12oz lager beer (stout seems to work too)
		<li>12g or so assorted dried hot peppers
	</ul>
	<h2>Instructions:</h2>
	<ol>
		<li>Brown the meat on high heat with minimal oil to give it a nice char
		<li>Chop the onion into chunks - I like them larger
		<li>Cut the peppers up into tiny pieces (I use scissors) and include the seeds
		<li>Add everything to a slow cooker and cook on high for 3 hours
		<li>Leave in the fridge overnight
	</ol>
